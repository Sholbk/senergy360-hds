'use client';

import Link from 'next/link';
import Image from 'next/image';
import { usePathname } from 'next/navigation';
import { cn } from '@/lib/utils';
import { useAuth } from '@/lib/hooks/useAuth';

interface NavItem {
  href: string;
  label: string;
  icon: React.ComponentType<{ className?: string }>;
  roles: ('admin' | 'property_owner' | 'architect' | 'general_contractor' | 'trade')[];
}

const navItems: NavItem[] = [
  { href: '/dashboard', label: 'Dashboard', icon: DashboardIcon, roles: ['admin', 'property_owner', 'architect', 'general_contractor', 'trade'] },
  { href: '/projects', label: 'Projects', icon: ProjectsIcon, roles: ['admin', 'property_owner', 'architect', 'general_contractor', 'trade'] },
  { href: '/calendar', label: 'Calendar', icon: CalendarIcon, roles: ['admin', 'property_owner', 'architect', 'general_contractor', 'trade'] },
  { href: '/organizations', label: 'Contacts', icon: OrganizationsIcon, roles: ['admin'] },
  { href: '/materials', label: 'Materials', icon: MaterialsIcon, roles: ['admin'] },
  { href: '/categories', label: 'Categories', icon: CategoriesIcon, roles: ['admin'] },
  { href: '/invoices', label: 'Invoices', icon: InvoicesIcon, roles: ['admin', 'property_owner'] },
  { href: '/settings/users', label: 'Users', icon: UsersIcon, roles: ['admin'] },
];

export default function AppSidebar() {
  const pathname = usePathname();
  const { role, loading } = useAuth();

  const visibleItems = navItems.filter(
    (item) => !role || item.roles.includes(role)
  );

  return (
    <aside className="w-[180px] min-w-[180px] bg-sidebar-bg min-h-screen flex flex-col">
      <div className="p-4 flex items-center gap-2">
        <Image src="/logo.png" alt="SENERGY360" width={32} height={32} className="w-8 h-8" />
        <span className="text-sidebar-text text-sm font-semibold tracking-wide">SENERGY360</span>
      </div>

      <nav aria-label="Main navigation" className="flex-1 px-2 py-4 space-y-1">
        {loading ? (
          <div className="px-3 py-2 text-sidebar-text text-xs opacity-50">Loading...</div>
        ) : (
          visibleItems.map((item) => {
            const isActive =
              pathname === item.href || pathname.startsWith(item.href + '/');
            return (
              <Link
                key={item.href}
                href={item.href}
                className={cn(
                  'flex items-center gap-3 px-3 py-2.5 rounded-md text-sm transition-colors',
                  isActive
                    ? 'bg-sidebar-active text-white font-medium'
                    : 'text-sidebar-text hover:bg-white/10'
                )}
              >
                <item.icon className="w-4 h-4 flex-shrink-0" />
                <span>{item.label}</span>
              </Link>
            );
          })
        )}
      </nav>
    </aside>
  );
}

