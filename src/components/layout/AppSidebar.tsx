'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { cn } from '@/lib/utils';

const navItems = [
  { href: '/dashboard', label: 'Dashboard', icon: DashboardIcon },
  { href: '/clients', label: 'Clients', icon: ClientsIcon },
  { href: '/projects', label: 'Projects', icon: ProjectsIcon },
  { href: '/professionals', label: 'Professionals', icon: ProfessionalsIcon },
  { href: '/materials', label: 'Materials', icon: MaterialsIcon },
  { href: '/categories', label: 'Categories', icon: CategoriesIcon },
];

export default function AppSidebar() {
  const pathname = usePathname();

  return (
    <aside className="w-[180px] min-w-[180px] bg-sidebar-bg min-h-screen flex flex-col">
      <div className="p-4 flex items-center gap-2">
        <div className="w-8 h-8 rounded-full bg-primary-light flex items-center justify-center">
          <span className="text-primary-dark text-xs font-bold">S</span>
        </div>
        <span className="text-sidebar-text text-sm font-semibold tracking-wide">SENERGY360</span>
      </div>

      <nav className="flex-1 px-2 py-4 space-y-1">
        {navItems.map((item) => {
          const isActive =
            pathname === item.href || pathname.startsWith(item.href + '/');
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                'flex items-center gap-3 px-3 py-2.5 rounded-md text-sm transition-colors',
                isActive
                  ? 'bg-sidebar-active text-white font-medium'
                  : 'text-sidebar-text hover:bg-white/10'
              )}
            >
              <item.icon className="w-4 h-4 flex-shrink-0" />
              <span>{item.label}</span>
            </Link>
          );
        })}
      </nav>
    </aside>
  );
}

